// Calculator.java
class Calculator {
  public int addition(int[] num) {

    int sum = num[0] + num[1];

    return sum;

  }

  public int subtraction(int[] num) {

    int sub = num[0] - num[1];

    return sub;

  }

  public int multiplication(int[] num) {

    int mul = num[0] * num[1];

    return mul;

  }

  public int division(int[] num) {

    int div = num[0] / num[1];

    return div;

  }
}
